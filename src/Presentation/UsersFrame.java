    private void buttonOpretActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOpretActionPerformed
        // TJEK OM TEKSTEN ER GYLDIG (INTS ETC..)
        User u = control.createUser(tfID.getText(), tfPass.getText(), cbLevel.getSelectedIndex()+1);
        model.addElement(u);
        control.saveUsers();
    }//GEN-LAST:event_buttonOpretActionPerformed

    private void buttonOpdaterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonOpdaterActionPerformed
        // TJEK OM TEKSTEN ER GYLDIG...
        User u = (User)listUsers.getSelectedValue();
        u.setPassword(tfPass.getText());
        u.setBrugerNiveau(cbLevel.getSelectedIndex()+1);
        control.registerDirtyUser(u);
        control.saveUsers();
    }//GEN-LAST:event_buttonOpdaterActionPerformed

    private void buttonSletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSletActionPerformed
        User u = (User)listUsers.getSelectedValue();
        control.registerRemovedUser(u);
        model.removeElement(u);
        control.saveUsers();
        tfID.setText(null);
        tfPass.setText(null);
    }//GEN-LAST:event_buttonSletActionPerformed

