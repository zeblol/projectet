    private void buttonPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonPrintActionPerformed
        ArrayList<OrderDetail> odl = new ArrayList();
        for (int i = 0; i < model.size(); i++) {
            odl.add((OrderDetail)model.get(i));
        }
        
        control.createNedskrivningPDF(odl, Double.parseDouble(tfPris.getText()), o);
    }//GEN-LAST:event_buttonPrintActionPerformed
    
    private void buttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSearchActionPerformed
        // Tjek om det indtastede tekst er et ordrenummer. Lav evt metode i Controlleren til det.
        o = control.getOrder(Integer.parseInt(tfOrdrenr.getText()));
        if(o != null){
            ArrayList<OrderDetail> odl = o.getOrderDetails();
            for (int i = 0; i < odl.size(); i++) {
                OrderDetail od = odl.get(i);
                mod.addElement(od);
            }
            listOrderDetails.setModel(mod);
        }
    }//GEN-LAST:event_buttonSearchActionPerformed

