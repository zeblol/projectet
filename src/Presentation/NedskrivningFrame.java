    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // Tjek om teksten i tfAmount er et tal.
        OrderDetail od = (OrderDetail) listOrderDetails.getSelectedValue();
        model.addElement(od);
        listNedskrevet.setModel(model);
        mod.removeElement(od);
        map.put(od.getpID(), od.getAmount());
        if (!tfAmount.getText().isEmpty()) {
            od.setAmount(Integer.parseInt(tfAmount.getText()));
        }
        tfAmount.setText(null);
    }//GEN-LAST:event_buttonAddActionPerformed

    private void buttonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRemoveActionPerformed
        OrderDetail od = (OrderDetail) listNedskrevet.getSelectedValue();
        model.removeElement(od);
        mod.addElement(od);
        if (map.containsKey(od.getpID())) {
            od.setAmount((int) map.get(od.getpID()));
            map.remove(od.getpID());
        }
    }//GEN-LAST:event_buttonRemoveActionPerformed
